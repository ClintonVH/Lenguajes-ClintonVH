        try {
             int edad=Integer.parseInt(textoEdad.getText());
            ValidarEdad.validar(edad);
            etiRes.setText("si me ves no se lanzo la excepcion");
        } catch (MenordeEdadException ex) {
            etiRes.setText(ex.getMessage());
            String cadena=ex.getMessage();
           JOptionPane.showOptionDialog(this,cadena, "Peque√±o Pervertido", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" Cancelar "},"Cancelar");;
        } catch(NumberFormatException ex){
            etiRes.setText("metiste letras y no un numero tonto");
            String cadena="metiste letras y no un numero tonto";
            JOptionPane.showOptionDialog(this,""+cadena , "La Cagaste Pendejo", JOptionPane.ERROR_MESSAGE, JOptionPane.ERROR_MESSAGE, null, new Object[]{" Cancelar "},"Cancelar");;
       
        }
